import mongoose from 'mongoose';

const healthTipSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  { timestamps: true }
);

const HealthTip = mongoose.model('HealthTip', healthTipSchema);
export default HealthTip;
